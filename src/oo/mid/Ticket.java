package oo.mid;

public class Ticket {
	int id;
	String name;
	float price;
	
	public int getId(){
		return id;
	}
	public String getName(){
		return name;
	}
	public float getPrice(){
		return price;
	}
	public void setId(int arg){
		id = arg;
	}
	public void setName(String arg){
		name = arg;
	}
	public void setPrice(float arg){
		price = arg;
	}
}
