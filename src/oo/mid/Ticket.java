package oo.mid;

public class Ticket {
	int id;
	String name;
	float price;
	
	public int getId(){
		return id;
	}
	public String getName(){
		return name;
	}
	public float getPrice(){
		return price;
	}
	public void setId(int id){
		this.id = id;
	}
	public void setName(String name){
		this.name = name;
	}
	public void setPrice(float price){
		this.price = price;
	}
}
