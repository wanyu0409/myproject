package oo.mid;

public class Meal {
	String meal;
	String drinks;
	String catering;
	int number;
	int prize;
	int total;
	
	public Meal(String meal, String drinks, String catering, int number, int prize,	int total){
		this.meal = meal;
		this.drinks = drinks;
		this.catering = catering;
		this.number = number;
		this.prize = prize;
		this.total = total;
	}
	
}
